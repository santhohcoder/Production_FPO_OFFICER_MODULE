package com.demo.fpo.model;

public class ReportCounts {
	
	private Long custitm;
	private Long arrival;
	private Long ooc;
	private Long delivery;
	private Long cusrsp;
	/**
	 * @return the custitm
	 */
	public Long getCustitm() {
		return custitm;
	}
	/**
	 * @param custitm the custitm to set
	 */
	public void setCustitm(Long custitm) {
		this.custitm = custitm;
	}
	/**
	 * @return the arrival
	 */
	public Long getArrival() {
		return arrival;
	}
	/**
	 * @param arrival the arrival to set
	 */
	public void setArrival(Long arrival) {
		this.arrival = arrival;
	}
	/**
	 * @return the ooc
	 */
	public Long getOoc() {
		return ooc;
	}
	/**
	 * @param ooc the ooc to set
	 */
	public void setOoc(Long ooc) {
		this.ooc = ooc;
	}
	/**
	 * @return the delivery
	 */
	public Long getDelivery() {
		return delivery;
	}
	/**
	 * @param delivery the delivery to set
	 */
	public void setDelivery(Long delivery) {
		this.delivery = delivery;
	}
	/**
	 * @return the cusrsp
	 */
	public Long getCusrsp() {
		return cusrsp;
	}
	/**
	 * @param cusrsp the cusrsp to set
	 */
	public void setCusrsp(Long cusrsp) {
		this.cusrsp = cusrsp;
	}

}
